int k, n, a[32], cnt;

void dfs(int x) {
	if(x == 32) {
		++cnt;
		if(cnt == n) {
			printf("Found\n");
			for(int i = 1; i <= 31; ++i)
				printf("%d ", a[i]);
			exit(0);
		}
		return;
	}
	if(a[x] != 0) {
		dfs(x + 1);
		return;
	}
	for(int i = 1; i <= k; ++i) {
		for(int j = 1; j <= dif[x][0]; ++j) {
			if(i == a[dif[x][j]]) goto ctin;
		}
		a[x] = i;
		dfs(x + 1);
		a[x] = 0;
		ctin:;
	}
}

