void dfs(int dep)
{
	if  (dep==32)
		{
			CNT++;
			if  (CNT==N)
				writeln();
			return;
		}
	if  (a[dep]!=0)
		{
			dfs(dep+1);
			return;
		}
	for  (int i=1;i<=K;i++)
		if  (hash[dep][i]==0)
			{
				b[dep]=i;
				for  (int j=0;j<TTT[dep];j++)
					hash[TT[dep][j]][i]++;
				dfs(dep+1);
				for  (int j=0;j<TTT[dep];j++)
					hash[TT[dep][j]][i]--;
			}
}
