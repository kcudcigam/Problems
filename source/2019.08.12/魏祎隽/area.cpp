int calc(int x,int y){
	if(max(x,y)>=16384)throw "FUCK";
	if(x==0 && y==0)return 1;
	int dig=max(lg2[x],lg2[y]);
	if((x>>dig&1) && (y>>dig&1))return calc(x^1<<dig,y^1<<dig)^1;
	else return calc(x&~(1<<dig),y&~(1<<dig));
}
void bfs(int x,int y){
	qx[1]=x,qy[1]=y;vis[x][y]=1;
	int l=1,r=1;
	while(l<=r){
		int x=qx[l],y=qy[l++],t=calc(x,y);
		cnt++;
		if(st.count({x,y}))
			printf("%d\n",18936023),exit(0);
		if(!x || !y)puts("infinity"),exit(0);
		if(calc(x,y-1)==t && !vis[x][y-1])vis[x][y-1]=1,qx[++r]=x,qy[r]=y-1;
		if(calc(x,y+1)==t && !vis[x][y+1])vis[x][y+1]=1,qx[++r]=x,qy[r]=y+1;
		if(calc(x-1,y)==t && !vis[x-1][y])vis[x-1][y]=1,qx[++r]=x-1,qy[r]=y;
		if(calc(x+1,y)==t && !vis[x+1][y])vis[x+1][y]=1,qx[++r]=x+1,qy[r]=y;
	}
}
