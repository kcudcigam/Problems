void read(long long &x)
{
	x=0;
	long long f=1;char ch=getchar();
    while(ch<'0'||ch>'9'){
		ch=getchar();
		if(ch=='-')f=-1;
	}
    while(ch>='0'&&ch<='9')x=x*10+ch-48,ch=getchar();
    x*=f;
}
